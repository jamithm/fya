    <h1 align="center">Reseña Historica de Cándida María de Jesús Fe y Alegría</h1>
    <p align="justify">La U.E Fe y Alegría "Cándida María de Jesús", ubicada en sector Dr. Noriega Trigo, de la Parroquia El Rosario, del Municipio Rosario de Perijá, en el Estado Zulia; es una  escuela perteneciente al Movimiento de Educación Popular Integral y Promoción Social (Fe y Alegría) y Sub-vencionada por la Asociación Venezolana de Educación Católica (AVEC),  a través del convenio  AVEC-MPPE (Ministerio del Poder Popular para la Educación).</p> 

 <p align="justify">Su fundación se remonta al año 1985, cuando un grupo de personas de Villa del Rosario y las hermanas de la Congregación "Hijas de Jesús",  entre otros, se unieron para consolidar un sueño en común: brindar a los niños, las niñas y los jóvenes de este sector una educación de calidad.</p>

<p align="justify">El nombre "Cándida María de Jesús"; ha sido en memoria de la fundadora de la Congregació "Hijas de Jesús",  una mujer sencilla que nació en Andoain (Guipúzcoa) España en el año 1845 y murió el 9 de agosto de 1912, luego de que dedicara su vida entera a la educación cristiana de la niñez y la juventud. Fue canonizada por el Papa Benedicto XVI el 17 de octubre de 2010.</p>

<p align="justify">Esta inserta en el Sector Noriega Trigo, donde hay diversidad de culturas: colombianos, etnias indígenas wayuu y barí, así como personas nativas de diversos lugares de Venezuela. La economía se basa en el comercio informal: venta de alimentos, comidas dulces, bebidas alcohólicas, entre otros. Así como la cría y venta  de animales ovinos y avícolas. Para otras familias sus ingresos provienen de salarios ganados como: jornaleros en  materas; domesticas en las casas del centro de la localidad, en un porcentaje menor, como secretarias, enfermeras (ros), educadores y otros profesionales.</p>

<p align="justify">El sector cuenta con 5 consejos comunales, 4 Barrio Adentro, Comedores Comunitarios, Mercal, Tres canchas Comunitarias, 4 Escuelas que imparten el Nivel de Educación Primaria, y 4 centros de Educación Inicial, todos de carácter público.</p>
